def switch_terms_for_dictionary() -> set[str]:
    """Все токены для KNOWN_TERMS / is_known_domain_word."""
    terms: set[str] = set()
    for phrase in (*SWITCH_ELEMENT_TERMS, *SWITCH_INFLECTIONS):
        normalized = phrase.lower().replace("ё", "е")
        terms.add(normalized)
        terms.update(part for part in normalized.split() if len(part) >= 3)
    return terms


def build_switch_glossary_for_llm() -> str:
    """Текстовый глоссарий для system prompt LLM."""
    return "\n".join(_SWITCH_GLOSSARY_LINES)
